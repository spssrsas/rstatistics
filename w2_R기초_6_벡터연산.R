# 함수 호출 -> 벡터에 적용 -> 함수를 벡터의 각 원소에 하나씩 처리

# 기존 프로그램의 순환 loop 처리를 자동 수행
1+2
# 함수 + 에 벡터 원소 단위로 수행
"+"(1,2) #1이라는 벡터, 2라는 벡터. 벡터의 길이는 모두 1(하나)
c(1,2,3) + c(4,5,6)

c(1,2,3) * c(4,5,6)
c(10,20,30) / c(2,4,6)
c(10,20,30) %% c(3,5,7)
c(10,20,30) %/% c(3,5,7)


#벡터의 길이가 서로 다른 경우
#재사용 규칙 (recycle rule)
