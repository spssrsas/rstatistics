#원소중 하나라도 TRUE 포함 확인
any(-3:3 > 0)
#원소 모두 TRUE 인지 확인
all(-3:3 > 0)

#연산시, 부동 소수점 수로 저장되어져서 매우 작은 값으로 저장됨
sqrt(2)^2 == 2
sqrt(2)^2 - 2
identical(sqrt(2)^2, 2)

#매우 작은 미세한 값은 무시
all.equal(sqrt(2)^2, 2)
all.equal(sqrt(2)^2, 3)
isTRUE(all.equal(sqrt(2)^2, 3))


