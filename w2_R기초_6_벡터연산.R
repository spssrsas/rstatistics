#논리 연산자
# ==, !=, <, >, <=, >=, !x(x가 아니다), x|y, x&y, isTRUE()
v <- pi
w <- 10/3

v == w
v != w
v > w
v < w
!(v  > w)
(v == w) | (v < w)
(v == w) & (v < w)
isTRUE(v == w)


